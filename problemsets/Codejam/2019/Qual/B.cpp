const double EPS = 1E-10;

inline int cmp(double x, double y = 0, double tol = EPS) {
    return (x <= y + tol) ? (x + tol < y) ? -1 : 0 : 1;
}

void solve() {
  bool f = 1;
  string a, b = ""; cin >> a;
  for (char &c: a) {
    if (c == '4') { c = '3'; b += "1"; f = 0; }
    else if (!f) b += "0";
  }
  cout << a << " " << b << endl;
}

int main() {
  //	setbuf(stdout, NULL);
 	cin.tie(0);
  ios::sync_with_stdio(0);

  int t; cin >> t;
  for (int i = 1; i <= t; i++) {
    cout << "Case #" << i << ": ";
    solve();
  }
}